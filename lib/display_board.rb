# Define display_board that accepts a board and prints
# out the current state.
def display_board(answers)
  puts " #{answers[0]} | #{answers[1]} | #{answers[2]} "
  puts "-----------"
  puts " #{answers[3]} | #{answers[4]} | #{answers[5]} "
  puts "-----------"
  puts " #{answers[6]} | #{answers[7]} | #{answers[8]} "
end
